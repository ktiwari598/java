public class SQlvsNoSQL {
    /**
     * When to choose SQL
     * If you want a more consistent system
     * If your system is read heavy
     * If your data has a well-defined structure and relationships between entities
     * Use Cases - Banking System, Ticket Booking system,  E-commerce platforms
     *
     *
     * When to choose NoSQL
     * If you want a more available system (high throughput and low latency)
     * If your system is write heavy
     * If your data is not well structured and may change from time to time
     * Use Cases - Real time Analytics, Social media Analytics, and Recommendation engines.
     */
}
